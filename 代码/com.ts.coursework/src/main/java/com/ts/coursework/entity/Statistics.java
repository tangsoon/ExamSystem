package com.ts.coursework.entity;

public class Statistics {
	
	private int examPaperSum;
	private int questionSum;
	private int answerSum;
	private int subjectSum;
		
	public int getExamPaperSum() {
		return examPaperSum;
	}
	public void setExamPaperSum(int examPaperSum) {
		this.examPaperSum = examPaperSum;
	}
	public int getQuestionSum() {
		return questionSum;
	}
	public void setQuestionSum(int questionSum) {
		this.questionSum = questionSum;
	}
	public int getAnswerSum() {
		return answerSum;
	}
	public void setAnswerSum(int answerSum) {
		this.answerSum = answerSum;
	}
	public int getSubjectSum() {
		return subjectSum;
	}
	public void setSubjectSum(int subjectSum) {
		this.subjectSum = subjectSum;
	}
}
