package com.ts.coursework.entity;
/**
 * 存放文件地址
 * @author ts
 *
 */
public class WorkFile {
	private int id;
	private String name;
	private String path;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	
}
